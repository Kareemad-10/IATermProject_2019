    /**
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code>
     * methods.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     * @throws java.lang.ClassNotFoundException
     * @throws java.sql.SQLException
     */
    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException, ClassNotFoundException, SQLException {
        response.setContentType("text/html;charset=UTF-8");
        try (PrintWriter out = response.getWriter()) {
            /* TODO output your page here. You may use following sample code. */
              String username = request.getParameter("hrName");
              String password = request.getParameter("hrPass");
              boolean HRState;
              HR hr = new HR();
              HR_IO hrio = new HR_IO();
              hr.setUserName(username);
              hr.setPassword(password);
              HRState = hrio.authenticate_HR(hr);
              if(HRState){
                HttpSession se = request.getSession();
                se.setAttribute("username", hr.getUserName());
                se.setAttribute("password", hr.getPassword());
                request.getRequestDispatcher("/HR_Home.jsp").forward(request, response);
              } else {
                out.print("Invalid User!");
                request.getRequestDispatcher("/HR_login.jsp").forward(request, response);
              }  
        }
    }



